"""# Predicting per capita income in 2020"""

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn import linear_model

# Load the dataset
data = pd.read_csv('canada_per_capita_income.csv')
data
# Prepare the data
X = data[['year']]
y = data['per capita income (US$)']

# create and train the model
reg = linear_model.LinearRegression()
reg.fit(X, y)

# Predict the income for the year 2020
income_2020 = reg.predict([[2020]])
print(income_2020)

# Visualize the data
plt.xlabel('Year')
plt.ylabel('Per Capita Income (US$)')
plt.scatter(data['year'], data['per capita income (US$)'], color='red', marker='+')

# Plot the regression line
plt.plot(data['year'], reg.predict(X), color='blue')

# Show the plot
plt.show()